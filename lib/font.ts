const LOWER_TO_UPPER = (ch: string) => ch.toUpperCase();

export function getGlyph(ch: string): string[] {
  if (GLYPHS[ch]) return GLYPHS[ch];
  const up = LOWER_TO_UPPER(ch);
  if (GLYPHS[up]) return GLYPHS[up];
  return GLYPHS["?"];
}

export function textWidth(text: string, scale: number, spacing = 1): number {
  const w = (FONT_W + spacing) * scale;
  return Math.max(0, text.length * w - spacing * scale);
}
